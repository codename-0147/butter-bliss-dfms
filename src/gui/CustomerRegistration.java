    private void reset() {
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField2.setText("");
        jTextField1.grabFocus();
        jTextField1.setEditable(true);
        jTable1.clearSelection();
        search();
    }

    private void search() {
        int sort = jComboBox1.getSelectedIndex();

        if (sort == 0) {
            loadCustomers("name", "ASC", jTextField1.getText());
        } else if (sort == 1) {
            loadCustomers("name", "DESC", jTextField1.getText());
        } else if (sort == 2) {
            loadCustomers("points", "ASC", jTextField1.getText());
        } else if (sort == 3) {
            loadCustomers("points", "DESC", jTextField1.getText());
        }
    }
