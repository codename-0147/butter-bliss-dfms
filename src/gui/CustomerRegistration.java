    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String mobile = jTextField1.getText();
        String name = jTextField2.getText();

        if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your mobile", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (!mobile.matches("^07[01245678]{1}[0-9]{7}$")) {
            JOptionPane.showMessageDialog(this, "Please enter valid mobile", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your first name", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {

            try {

                ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `customer` WHERE `mobile` = '" + mobile + "'");

                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "Customer already registerd", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    MySQL.executeIUD("INSERT INTO `customer`(`name`,`mobile`,`points`)"
                            + "VALUES(" + name + "','" + mobile + "','0')");

                    loadCustomers("name", "ASC", jTextField1.getText());
                    reset();
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

    int row = jTable1.getSelectedRow();

    String mobile = String.valueOf(jTable1.getValueAt(row, 0));
    String name = String.valueOf(jTable1.getValueAt(row, 1));

    jTextField1.setText(mobile);
    jTextField1.setEditable(false);
    jTextField2.setText(name);

    if (evt.getClickCount() == 2) {
        try {
            ResultSet rs = MySQL.executeSearch("SELECT `id`, `points` FROM `customer` WHERE `mobile` = '" + mobile + "'");
            if (rs.next()) {
                String customerId = rs.getString("id");
                String points = rs.getString("points");

                // Send data to invoice
                if (invoice != null) {
                    invoice.getjLabel12().setText(name); // Customer name
                    invoice.getcustomerMobileField().setText(mobile); // Mobile
                    invoice.getjTextField4().setText(points); // Points
                }

                // Update invoice count label
                ResultSet invoiceCount = MySQL.executeSearch("SELECT COUNT(id) FROM `customer_invoice` WHERE `customer_id` = '" + customerId + "'");
                if (invoiceCount.next()) {
                    jLabel8.setText(invoiceCount.getString(1));
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

        this.dispose();
    }

    }//GEN-LAST:event_jTable1MouseClicked

