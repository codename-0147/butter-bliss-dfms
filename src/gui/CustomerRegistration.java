    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String mobile = jTextField1.getText().trim();
        String name = jTextField2.getText().trim();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter mobile number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `customer` WHERE `mobile` = '" + mobile + "'");

                if (resultSet.next()) {
                    // Mobile exists — update name
                    MySQL.executeIUD("UPDATE `customer` SET `name` = '" + name + "' WHERE `mobile` = '" + mobile + "'");
                    JOptionPane.showMessageDialog(this, "Customer name updated successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    // Mobile doesn't exist — insert new customer
                    MySQL.executeIUD("INSERT INTO `customer` (`mobile`, `name`) VALUES ('" + mobile + "', '" + name + "')");
                    JOptionPane.showMessageDialog(this, "New customer added", "Success", JOptionPane.INFORMATION_MESSAGE);
                }

                loadCustomers("name", "ASC", mobile);
                reset();

            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Database error", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

